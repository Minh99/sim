            <h3 dir="ltr" style="text-align:justify"><span style="font-size:16px"><strong><span style="font-family:arial,helvetica,sans-serif">b. C&aacute;ch chọn sim hợp mệnh Hỏa theo yếu tố &Acirc;m Dương</span></strong>
                                        </span>
            </h3>

            <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Sim phong thủy hợp mệnh Hỏa theo &Acirc;m Dương l&agrave; d&atilde;y số điện thoại mang năng lượng c&acirc;n bằng với &Acirc;m Dương mệnh, tức Hỏa &Acirc;m n&ecirc;n d&ugrave;ng sim vượng Dương, Hỏa Dương n&ecirc;n d&ugrave;ng sim vượng &Acirc;m.</span></span>
            </p>

            <p dir="ltr" style="text-align:justify"><em><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Để x&aacute;c định năng lượng &Acirc;m - Dương trong sim, &Acirc;m Dương mệnh bạn c&oacute; thể l&agrave;m theo c&aacute;c bước sau:</span></span></em></p>

            <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif"><strong>Bước 1: </strong>X&eacute;t t&iacute;nh &Acirc;m Dương trong nội tại sim:&nbsp;</span></span>
            </p>

            <ul>
                <li dir="ltr">
                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Nếu số lượng số lẻ (Dương) của một sim nhiều hơn số lượng số chẵn (&Acirc;m), th&igrave; sim đ&oacute; mang năng lượng Dương. Nếu sim c&oacute; 6 số lẻ (v&agrave; 4 số chẵn), th&igrave; đ&oacute; l&agrave; sim Vượng Dương v&agrave; c&oacute; độ c&acirc;n đối tốt. Tuy nhi&ecirc;n, nếu sim c&oacute; nhiều hơn 7 số lẻ, th&igrave; sẽ bị thi&ecirc;n lệch Dương.</span></span>
                    </p>
                </li>
                <li dir="ltr">
                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Ngược lại, nếu số lượng số chẵn (&Acirc;m) của một sim nhiều hơn số lượng số lẻ (Dương), th&igrave; sim đ&oacute; mang năng lượng &Acirc;m. Nếu sim c&oacute; 6 số chẵn (v&agrave; 4 số lẻ), th&igrave; đ&oacute; l&agrave; sim Vượng &Acirc;m v&agrave; c&oacute; độ c&acirc;n đối tốt. Tuy nhi&ecirc;n, nếu sim c&oacute; nhiều hơn 7 số chẵn, th&igrave; sẽ bị thi&ecirc;n lệch &Acirc;m.</span></span>
                    </p>
                </li>
                <li dir="ltr">
                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Nếu sim c&oacute; số lượng số chẵn v&agrave; số lượng số lẻ bằng nhau (5 số chẵn v&agrave; 5 số lẻ), th&igrave; đ&oacute; l&agrave; sim đạt c&acirc;n bằng tuyệt đối.</span></span>
                    </p>
