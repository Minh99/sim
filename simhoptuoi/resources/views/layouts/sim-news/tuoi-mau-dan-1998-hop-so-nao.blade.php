                    <h2 dir="ltr" style="text-align:justify"><span style="color:#FF0000"><strong><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif"><span id="vi-kham-pha-con-so-may-man-cho-tuoi-mau-dan-1998">VI. Khám phá con số may mắn cho tuổi mậu dần 1998</span></span>
                                </span>
                            </strong>
                        </span>
                    </h2>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Con số may mắn chưa chắc đã là con số hợp với tuổi mậu dần, tuy nhiên đa số người tuổi Mậu Dần thường nhầm lẫn hai con số này với nhau. Số may mắn cho tuổi 1998 thường được lựa chọn theo 3 cách sau:</span></span>
                    </p>

                    <h3 dir="ltr" style="text-align:justify"><em><strong><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">1. Con số may mắn của tuổi mậu dần</span></span></strong></em></h3>

                    <h4 dir="ltr" style="text-align:justify"><em><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">a. <u>Theo mệnh quái tuổi 1998 hợp số nào thì may mắn?</u></span></span></em></h4>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Mệnh quái là yếu tố được xác định dựa theo năm sinh giới tính của người tuổi Mậu Dần, con số may mắn theo mệnh quái là con số tương sinh với ngũ hành cung phi của người dùng. Cụ thể, con số may mắn theo mệnh quái tương ứng với người sinh năm 1998 như sau:</span></span>
                    </p>

                    <ul>
                        <li dir="ltr">
                            <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Nữ mạng sinh năm 1998 cung Tốn - ngũ hành Mộc nên hợp với các con số thuộc ngũ hành Thủy: số 1, số 0</span></span>
                            </p>
                        </li>
                        <li dir="ltr">
                            <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Nam mạng tuổi Mậu Dần cung Khôn - ngũ hành Thổ nên nam mạng tuổi 1998 hợp với con số thuộc ngũ hành Hỏa: số 9.</span></span>
                            </p>
                        </li>
                    </ul>

                    <h4 dir="ltr" style="text-align:justify"><em><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">b. <u>1998 hợp số nào thì may mắn theo số sinh?</u></span></span></em></h4>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Theo cách tính số may mắn theo số sinh thì người tuổi Mậu Dần sẽ tìm được con số may mắn khi sử dụng tổng ngày tháng năm sinh rồi chia cho 9, số may mắn cho người sinh năm 1998 chính là số dư của phép chia.</span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Ví dụ: Người sinh ngày 12/08/1998 thì con số may mắn theo số sinh như sau:</span></span>
                    </p>

                    <ul>
                        <li dir="ltr">
                            <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Bước 1: Tính tổng bát tự ngày sinh:</span></span>
                            </p>
                        </li>
                    </ul>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">1 + 2 + 8 + 1 + 9 + 9 + 8 = 38</span></span>
                    </p>

                    <ul>
                        <li dir="ltr">
                            <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Bước 2: Tìm số may mắn theo số sinh</span></span>
                            </p>
                        </li>
                    </ul>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">38 :  9 = 4  dư 2 → số may mắn của người tuổi Mậu Dần sinh ngày 12 tháng 8 là số 2.</span></span>
                    </p>

                    <h4 dir="ltr" style="text-align:justify"><em><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">c. <u>Số may mắn của tuổi mậu dần theo năm sinh và giới tính</u></span></span></em></h4>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Số may mắn theo năm sinh và giới tính cho người sinh năm 1998 tuổi Mậu Dần như sau:</span></span>
                    </p>

                    <ul>
                        <li dir="ltr">
                            <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Bước 1: Tính tổng nút của năm sinh: 9 + 8 = 17 = 1 + 7 = 8</span></span>
                            </p>
                        </li>
                        <li dir="ltr">
                            <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Bước: Tìm số may mắn cho nam mạng tuổi Mậu Dần: 10 - 8 = 2</span></span>
                            </p>
                        </li>
                    </ul>

                    <ul>
                        <li dir="ltr">
                            <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Bước 3: Tìm số may mắn cho nữ mạng tuổi Mậu Dần: 8 + 5 = 13 = 1 + 3 = 4</span></span>
                            </p>
                        </li>
                    </ul>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Vậy, số may mắn của nam mạng sinh năm 1998 theo năm sinh và giới tính là 2, nữ mạng là số 4.</span></span>
                    </p>

