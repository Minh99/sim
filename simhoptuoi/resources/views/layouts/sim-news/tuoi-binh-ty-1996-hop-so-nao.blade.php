                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Xét theo ngũ hành bản mệnh Giản Hạ Thủy thì người sinh năm 1996 hợp với những con số thuộc ngũ hành Kim: số 6, số 7. Đồng thời những con số thuộc ngũ hành Thủy như: số 0, số 1 cũng sẽ mang lại nhiều may mắn cho người tuổi Bính Tý. Đặc biệt, người sinh năm 1996 bản mệnh Thủy nên cần tránh những con số thuộc ngũ hành Thổ (xung khắc) bản mệnh gồm: số 2, số 5 và số 8.</span></span>
