                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Tương tự ứng dụng cân bằng âm dương vào chọn sim thì người tuổi Bính Tý nên lựa chọn dãy sim tương đối cân bằng lệch âm (6 số chẵn : 4 số lẻ) hoặc dãy sim cân bằng tuyệt đối (5 số chẵn : 5 số lẻ).</span></span>
