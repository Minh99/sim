                    <h4 dir="ltr" style="text-align:justify"><em><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">a. <u>Theo mệnh quái tuổi 1996 hợp số nào thì may mắn?</u></span></span></em></h4>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Luận giải theo năm sinh và giới tính để xác định được cung phi, dựa vào cung phi để suy ra ngũ hành và con số may mắn tương ứng. Cụ thể, người tuổi Bính Tý sinh năm 1996 nên nam mạng thuộc cung Tốn (ngũ hành Mộc), nữ mạng cung Khôn (ngũ hành Thổ). Con số may mắn cho tuổi 1996 theo mệnh quái như sau:</span></span>
