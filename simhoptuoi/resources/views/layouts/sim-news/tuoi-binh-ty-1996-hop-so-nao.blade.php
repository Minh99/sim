                    <h4 dir="ltr" style="text-align:justify"><em><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">b. <u>Theo số sinh tuổi Bính Tý hợp số nào thì may mắn?</u></span></span></em></h4>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Dựa vào bát tự ngày sinh của người tuổi Bính Tý có thể tìm ra được con số may mắn tương ứng với mỗi người. Cộng dồn các số trong ngày, tháng, năm sinh rồi chia 9, số dư của phép chia chính là số may mắn cho người dùng tuổi 1996.</span></span>
