                    </p>

                    <ul>
                        <li dir="ltr">
                            <p dir="ltr" style="text-align:justify"><strong><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Con số may mắn của nữ tuổi Tân Mùi 1991</span></span></strong></p>
                        </li>
                    </ul>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Nữ mạng tuổi Tân Mùi 1991 cung Càn cũng thuộc ngũ hành Thổ giống như nam giới. Do đó, con số may mắn với nữ Tân Mùi là số 9 và con số tương khắc là số 3, 4.</span></span>
                    </p>

                    <h4 dir="ltr" style="text-align:justify"><em><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">b. <u>Con số may mắn tuổi Tân Mùi theo số sinh</u></span></span></em></h4>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Cách tính này khá đơn giản, người dùng chỉ cần lấy các con số trong ngày tháng năm sinh cộng lại với nhau. Sau đó, lấy tổng chia cho 9, dư bao nhiêu thì đó chính là con số hợp tuổi.</span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Ví dụ, người sinh ngày 15 tháng 08 năm 1991 sẽ tính con số sinh như sau:</span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Lấy 1 + 5 + 8 + 1+ 9 +1 = 24. Tiếp tục lấy 24 chia cho 9 được 2 dư 6. Vậy con số may mắn của người sinh ngày 15/8/1991 là con số 6.</span></span>
                    </p>

                    <h4 dir="ltr" style="text-align:justify"><em><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">c. <u>Theo năm sinh và giới tính tuổi Tân Mùi nên dùng con số nào?</u></span></span></em></h4>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Dựa theo cách tính này, con số may mắn của tuổi Tân Mùi 1991 được tính như sau:</span></span>
                    </p>

                    <ul>
                        <li dir="ltr">
                            <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif"><strong>Bước 1:</strong> Lấy hai con số cuối cùng trong năm sinh cộng lại với nhau để ra tổng a. Nếu a lớn hơn 9 thì tiếp tục cộng hai số với nhau để ra tổng b.</span></span>
                            </p>
                        </li>
                    </ul>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Ví dụ, ta lấy 9+1 = 10, vì 10 lớn hơn 9 nên tiếp tục lấy 1 + 0 = 1.</span></span>
                    </p>

                    <ul>
                        <li dir="ltr">
                            <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif"><strong>Bước 2: </strong>Nếu là nam mạng thì lấy 10 trừ đi tổng b được kết quả c. Nếu là nữ mạng thì lấy 5 cộng với tổng b được d. Trong trường hợp, d lớn hơn 9 thì lại tiếp tục cộng 2 con số với nhau để ra kết quả cuối cùng chỉ còn 1 con số. Lúc này, con số c và d là con số may mắn của nam và nữ tuổi Tân Mùi 1991.</span></span>
                            </p>
                        </li>
                    </ul>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Tiếp tục ví dụ ở trên, nếu người dùng là nam mạng tuổi 1991 thì con số may mắn sẽ là: 10-1 = 9. Nếu người dùng là nữ mạng thì con số may mắn sẽ là: 5 + 1 = 6.</span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"> </p>

                    <p dir="ltr" style="text-align:center"> </p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Sử dụng con số hợp tuổi mang đến nhiều tác động tích cực cho tuổi Tân Mùi 1991 trên nhiều phương diện khác nhau. Tuy nhiên, người dùng khi nắm rõ người <strong>sinh năm 1991 hợp số nào</strong> cần lưu ý rằng những con số này chỉ may mắn khi đứng riêng lẻ một mình, không phải là cơ sở để lựa chọn sim phong thủy, biển số xe… hợp với tuổi Tân Mùi 1991. Nắm rõ điều này sẽ giúp chủ nhân ứng dụng một cách linh hoạt và thích hợp vào thực tiễn.</span></span>
                    </p>

