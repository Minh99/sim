                    <h4 dir="ltr" style="text-align:justify"><em><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">a. <u>Theo mệnh quái người sinh năm 1991 nên dùng số nào?</u></span></span></em></h4>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Mệnh quái là cung mệnh được xác định dựa vào năm sinh và giới tính của mỗi người. Khác với mệnh niên, mệnh quái có sự thay đổi giữa nam và nữ. Có nghĩa là, cùng một năm sinh nhưng nam và nữ có những mệnh quái khác nhau. Phương pháp chọn con số may mắn cho tuổi 1991 theo mệnh quái được xác định theo mối quan hệ ngũ hành sinh khắc của con số với mệnh quái của tuổi. Cụ thể như sau:</span></span>
