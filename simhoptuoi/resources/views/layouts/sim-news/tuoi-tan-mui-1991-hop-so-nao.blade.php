                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Con số may mắn không phải là con số hợp tuổi Tân Mùi 1991. Bởi số hợp tuổi 1991 có căn cứ lựa chọn theo phong thủy, được chuyên gia khuyên dùng. Còn con số may mắn với tuổi 1991 được lựa chọn theo nhiều phương pháp truyền miệng khác nhau, chưa có cơ sở xác định rõ ràng.</span></span>
