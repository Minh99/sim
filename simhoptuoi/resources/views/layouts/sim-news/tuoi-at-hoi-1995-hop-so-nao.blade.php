                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Số hợp tuổi Ất Sửu là những con số có ngũ hành tương sinh, tương hỗ với bản mệnh. Tính theo bảng tuổi âm lịch 60 Hoa Giáp thì người sinh năm 1995 bản mệnh Sơn Đầu Hỏa nên con số hợp tuổi 1995 phải là con số thuộc ngũ hành Mộc - tương sinh bản mệnh Hỏa. Các con số thuộc ngũ hành Mộc có 1 chữ số gồm: số 3 và số 4. Ngoài ra, các con số thuộc ngũ hành Hỏa tương trợ mệnh niên Sơn Đầu Hỏa của người dùng cũng có thể mang lại thay đổi tích cực khi sử dụng là số 9.</span></span>
