                            </p>
                        </li>
                        <li dir="ltr">
                            <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif"><strong>B3: </strong>Nếu số D > 9 thì đem cái số trong D cộng dồn được kết quả là số E. </span></span>
