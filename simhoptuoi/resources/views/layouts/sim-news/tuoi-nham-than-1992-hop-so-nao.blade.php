                    <h4 dir="ltr" style="text-align:justify"><em><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">3.1 <u>Số hợp mệnh kim 1992 là những số nào?</u></span></span></em></h4>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Dựa vào mối quan hệ ngũ hành con số với bản mệnh của người tuổi Nhâm Thân có thể biết được chủ mệnh tuổi này hợp số nào. Đây là cách xác định con số hợp mệnh kim 1992 bằng mệnh niên. Cụ thể, người sinh năm 1992 có ngũ hành bản mệnh là Kiếm Phong Kim, theo sự hợp - khắc của ngũ hành thì Thổ sinh Kim. Do đó, nam nữ bản mệnh Kiếm Phong Kim tuổi Thân nên sử dụng các con số có ngũ hành Thổ bao gồm: số 2; 5 và 8, giúp đem lại may mắn và tài lộc. Bên cạnh đó, người tuổi này cũng có thể sử dụng các con số thuộc hành Kim là 6 hoặc 7. Bởi con số này có ngũ hành tương trợ cho chính bản mệnh của chủ nhân. </span></span>
                    </p>

