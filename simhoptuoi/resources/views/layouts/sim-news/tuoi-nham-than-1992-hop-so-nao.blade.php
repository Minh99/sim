                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Căn cứ vào năm sinh và giới tính của chủ mệnh thì trong dân gian lưu truyền một phép tính con số may mắn của tuổi 1992 theo các bước như sau: </span></span>
                    </p>

                    <ul>
                        <li dir="ltr">
                            <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif"><strong>B1:</strong>  Lấy hai số cuối của năm sinh cộng lại với nhau  = tổng số A. Nếu A > 9 thì tiếp tục cộng các số với nhau ta được tổng số B. </span></span>
                            </p>
                        </li>
                        <li dir="ltr">
