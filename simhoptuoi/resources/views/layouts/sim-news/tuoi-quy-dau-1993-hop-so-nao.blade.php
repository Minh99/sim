                    <h2 dir="ltr" style="text-align:justify"><span style="color:#FF0000"><strong><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif"><span id="iv--lam-tuong-khi-xem-tuoi-quy-dau-1993-hop-so-nao">IV.  Lầm tưởng khi xem tuổi Quý Dậu 1993 hợp số nào?</span></span>
                                </span>
                            </strong>
                        </span>
                    </h2>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Con số may mắn luôn được quan tâm và tìm hiểu bởi con người luôn mong muốn ứng dụng các con số này vào cuộc sống nhằm gia tăng may mắn cho chính mình. Một số phương diện được nhiều người sử dụng con số may mắn bao gồm: số nhà, biển số xe, số tài khoản ngân hàng, số thẻ,.... Hiện nay, nhiều người cho rằng con số hợp tuổi 1993 chính là con số may mắn. Vậy điều này liệu có đúng hay không và làm cách nào để xác định con số may mắn thuộc về mình? </span></span>
                    </p>

                    <h3 dir="ltr" style="text-align:justify"><em><strong><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">1. Số may mắn của tuổi quý dậu có phải số hợp tuổi 1993?</span></span></strong></em></h3>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Các chuyên gia của số may mắn và số hợp tuổi là hai loại số khác nhau. Nguyên nhân của sự khác nhau này xuất phát từ cách thức xác định của chúng. Để tính số hợp tuổi người sinh năm 1993, ta dựa trên cơ sở thuyết ngũ hành âm dương và được khuyên nên sử dụng. Ngược lại, con số may mắn của tuổi quý dậu được chọn lựa dựa trên những lưu truyền từ đời này sang đời khác chứ không có cơ sở khoa học chính xác. </span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"> </p>

                    <p dir="ltr" style="text-align:center"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif"><img alt="1993 hợp số nào" src="https://simhoptuoi.com.vn/media/images/news/336/1993-hop-so-nao-1.png" /></span></span><br />
                    </p>

                    <h3 dir="ltr" style="text-align:justify"><em><strong><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">2. Các cách chọn số may mắn của tuổi quý hợi phổ biến hiện nay</span></span></strong></em></h3>

                    <h4 dir="ltr" style="text-align:justify"><em><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">a. <u>Theo mệnh quái tuổi 1993 hợp số nào thì may mắn?</u></span></span></em></h4>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Một trong số các phương pháp tìm số may mắn cho người sinh năm 1993 đang phổ biến và được sử dụng nhiều nhất hiện nay đó là tính dựa theo mệnh quái. Mệnh quái được gọi với cái tên khác là cung mệnh và được xác định thông qua năm sinh và giới tính của người đó. Tính con số may mắn theo cung mệnh chính là cách suy luận thông qua mối quan hệ tương sinh tương khắc của ngũ hành mệnh quái tuổi đó.</span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Với nam nữ sinh năm 1993, nam giới tuổi Dậu có cung phi Đoài - hành Kim cho nên con số may mắn cho họ sẽ là số 6, 7, 2, 5 và 8 thuộc hành Kim và hành Thổ do Thổ sinh Kim. Còn đối với với người nữ tuổi Quý Dậu có cung phi Cấn - hành Thổ, được biết hành  Hỏa sinh Thổ nên người nữ tuổi này nên sử dụng con số may mắn thuộc hành Thổ và Hỏa bao gồm số 2, 5, 8 và 9.</span></span>
                    </p>

                    <h4 dir="ltr" style="text-align:justify"><em><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">b. <u>Theo số sinh tuổi quý dậu hợp với số nào thì may mắn?</u></span></span></em></h4>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Việc tìm con số may mắn theo số sinh là cách tính cộng dồn các chữ số trong bát tự ngày tháng năm sinh của chủ sở hữu, sau đó lấy tổng này chia cho 9. Số dư của phép tính này chính là số may mắn mà bạn đang tìm kiếm. </span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Ví dụ: Để tính con số may mắn của người sinh ngày 25/01/1993 theo số sinh ta làm như sau:</span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">(2 + 5 +1 + 1 + 9 + 9 +3) / 9 = 3 dư 3</span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">⇒ Kết quả của phép tính trên dư 3 do đó, số may mắn của người sinh ngày này chính là số 3. </span></span>
                    </p>

                    <h4 dir="ltr" style="text-align:justify"><em><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">c. <u>Theo năm sinh và giới tính tuổi quý dậu hợp số mấy thì may mắn?</u></span></span></em></h4>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Thông qua năm sinh và giới tính được nhân dân truyền miệng lại thì nam nữ tuổi Quý Dậu có thể xác định số may mắn như sau:</span></span>
                    </p>

                    <ul>
                        <li dir="ltr">
                            <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">B1:  Hai số cuối năm sinh cộng lại được tổng A, đem so sánh số A với 9 nếu lớn hơn thì tiếp tục cộng số A này lại với nhau được tổng B. </span></span>
                            </p>
                        </li>
                        <li dir="ltr">
                            <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">B2: Đối với nam mạng tuổi 1993 ta lấy 10 - B = C. Còn đối với nữ giới Quý Dậu ta lấy 5 + C = D. </span></span>
                            </p>
                        </li>
                        <li dir="ltr">
                            <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">B3: So sánh số D với 9 nếu D > 9 ta đem cộng dồn các số trong D được kết quả là E. </span></span>
                            </p>
                        </li>
                    </ul>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">⇒ Kết luận: Số may mắn của nam giới là số C và nữ giới là số E. </span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><em><strong><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Ví dụ: Để xác định con số may mắn cho nam nữ tuổi Dậu 1993 bằng phương pháp dựa trên  năm sinh và giới tính ta tiến hành các bước như sau:</span></span></strong></em></p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Bước 1: Lấy 9 + 3 = 12 > 9, do đó ta tiếp tục công: 1 + 2 = 3</span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Bước 2: </span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">+ Đối với nam giới Quý Dậu: 10 - 3 = 7</span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">+ Đối với nữ giới Quý Dậu: 5 + 3 = 8 < 9</span> </span> </p> <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Như vậy, con số may mắn của nam tuổi 1993 là 9 và nữ giới tuổi 1993 là 8. </span></span>
                    </p>

