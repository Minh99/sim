                            <p style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif"><strong>Âm - Dương:</strong> Xác định mức độ cân bằng về năng lượng Âm Dương của số sim hợp tuổi Quý Dậu theo số chẵn : lẻ, tốt nhất là dãy sim có tỷ lệ là  5 : 5 Nếu rơi vào trường hợp 4 : 6 hoặc 6 : 4 thì ta xét đến âm - dương của bản mệnh người dùng. Nam nữ tuổi 1993 là Âm mạng do đó nên sử dụng dãy số vượng Dương (nhiều số lẻ) để cân bằng năng lượng của dãy sim và chủ sở hữu. </span></span>
