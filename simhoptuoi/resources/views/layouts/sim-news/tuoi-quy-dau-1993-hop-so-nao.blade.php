                            <p style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif"><strong>Quẻ dịch:</strong> Việc luận giải một số sim là Hung hay Cát dựa trên việc phân tích và đánh giá quẻ dịch bao gồm: quẻ Chủ và quẻ Hỗ. Người sinh năm 1993 nên chọn những dãy sim có cả 2 quẻ đều tốt. Ngoài ra, họ cũng có thể chọn những quẻ dịch có ít nhất 1 quẻ tốt cho tài lộc hoặc tình duyên,... tùy vào mục đích sử dụng. </span></span>
