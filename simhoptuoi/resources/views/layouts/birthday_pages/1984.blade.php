                                            dư chính là con số sinh mà bạn cần tìm</span></span></p>
                            </li>
                        </ul>

                        <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span
                                    style="font-family:arial,helvetica,sans-serif">Ví dụ: Người sinh ngày 12 tháng 9 năm
