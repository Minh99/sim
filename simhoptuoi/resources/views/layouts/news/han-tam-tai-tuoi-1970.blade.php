                        <p dir="ltr"><span style="font-size:16px"><span style="font-family:times new roman,times,serif">Dựa theo bảng nguyên lý trên thì tuổi 1970 thuộc nhóm Tam hợp Kim cục Dần, Ngọ, Tuất thì tam tai sẽ rơi vào 3 năm liên tiếp Thân, Dậu, Tuất. Tính theo lịch vạn niên thì 3 năm tam tai tuổi Canh Tuất sẽ là 1992, 1993, 1994, 2004, 2005, 2006, 2016, 2017, 2018, 2028, 2029, 2030,... Và vòng quay này sẽ được lặp lại sau 12 năm 1 lần.</span></span>
                        </p>

                        <h3 dir="ltr"><strong><em><span style="font-size:16px"><span style="font-family:times new roman,times,serif">2.2. Người tuổi 1970 có phạm tam tai trong năm 2023 không?</span></span></em></strong></h3>

                        <p dir="ltr"><span style="font-size:16px"><span style="font-family:times new roman,times,serif">Tương tự dựa theo bảng tam tai ở trên, chủ mệnh tuổi Canh Tuất nhận biết được năm 2023 không phải năm tam tai của 1970. Bởi năm 2023 là năm Mão không thuộc nhóm tam tai Thân - Dậu - Tuất của tuổi 1970.</span></span>
                        </p>

                        <p dir="ltr"><span style="font-size:16px"><span style="font-family:times new roman,times,serif">Trong năm 2023 những tuổi phạm tam tai sẽ bao gồm 3 tuổi là Thân, Tý và Thìn. Cụ thể là những người có năm sinh âm lịch là 1960 (Canh Tý), 1972 (Nhâm Tý), 1984 (Giáp Tý), 1996 (Bính Tý), 2008 (Mậu Tý), 1964 (Giáp Thìn), 1976 (Bính Thìn), 1988 (Mậu Thìn), 2000 (Canh Thìn), 1968 (Mậu Thân), 1980 (Canh Thân), 1992 (Nhâm Thân), 2004 (Giáp Thân). </span></span>
                        </p>

                        <h3 dir="ltr"><strong><em><span style="font-size:16px"><span style="font-family:times new roman,times,serif">2.3. Năm tam tai tuổi Tuất 1970 nào nặng nhất? </span></span></em></strong></h3>

                        <p dir="ltr"><span style="font-size:16px"><span style="font-family:times new roman,times,serif">Theo các chuyên gia phong thủy,  trong 3 năm tam tai của tuổi Canh Tuất thì thì năm Dậu là năm tam tai nặng nhất. Hai năm tam tai Thân và Tuất thì tuổi 1970 sẽ chịu ảnh hưởng nhẹ hơn. Đồng thời các chuyên gia cũng đưa ra một số lời khuyên từng năm tam tai tuổi Canh Tuất như sau:</span></span>
                        </p>

                        <p dir="ltr"><span style="font-size:16px"><span style="font-family:times new roman,times,serif">- Năm Thân là năm tam tai đầu của tuổi Canh Tuất 1970, không nên khởi sự việc trọng đại như xây nhà, cưới hỏi, đầu tư kinh doanh lớn. Thay vào đó quý bạn tuổi 1970 chỉ nên tiếp tục triển khai công việc hiện tại. </span></span>
                        </p>

                        <p dir="ltr"><span style="font-size:16px"><span style="font-family:times new roman,times,serif">- Năm Dậu là năm tam tai giữa, còn gọi là năm tam tai nặng nhất của tuổi Canh Tuất. Trong năm này thì quý bạn không nên dừng các công việc đang triển khai. Nếu dừng, mai này rất khó để quay trở lại. </span></span>
                        </p>

                        <p dir="ltr"><span style="font-size:16px"><span style="font-family:times new roman,times,serif">- Năm Tuất là năm tam tai cuối,  quý bạn tuổi Canh Tuất cần lưu ý không nên kết thúc những việc lớn. Càng về cuối năm này thì tài lộc của tuổi 1970 sẽ bắt Tuất quay trở lại nhiều hơn, do càng gần tới việc kết thúc hạn tam tai tuổi Canh Tuất. </span></span>
                        </p>

