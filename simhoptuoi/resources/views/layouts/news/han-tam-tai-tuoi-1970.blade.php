                        <h3 dir="ltr"><strong><em><span style="font-size:16px"><span style="font-family:times new roman,times,serif">4.1. Xác định vị thần, ngày và hướng cúng giải hạn tam tai 1970</span></span></em></strong></h3>

                        <p dir="ltr"><span style="font-size:16px"><span style="font-family:times new roman,times,serif">Căn cứ theo địa chi của mỗi năm âm lịch sẽ tính được vị thần đại diện tương ứng với ngày cúng và hướng vái lạy cho người tuổi 1970 mong muốn giải hạn tam tai. Cụ thể như sau:</span></span>
                        </p>

                        <p dir="ltr"><span style="font-size:16px"><span style="font-family:times new roman,times,serif">- Năm tam tai 1970 là Thân do Nhân Hoàng Thần Quang đại diện nên cúng vào ngày 08 âm lịch, hướng vái lạy Tây Nam. </span></span>
                        </p>

                        <p dir="ltr"><span style="font-size:16px"><span style="font-family:times new roman,times,serif">- Năm hạn tam tai tuổi Canh Tuất là Dậu do Thiên Họa Thần Quang đại diện nên cúng vào ngày 07 âm lịch, hương vái lạy chính Tây.</span></span>
                        </p>

                        <p dir="ltr"><span style="font-size:16px"><span style="font-family:times new roman,times,serif">- Năm tam tai tuổi Tuất 1970 là Tuất do Địa Tài Thần Quang đại diện nên cúng vào ngày 06 âm lịch, hướng vái lạy Tây Bắc. </span></span>
                        </p>

                        <h3 dir="ltr"><strong><em><span style="font-size:16px"><span style="font-family:times new roman,times,serif">4.2. Chuẩn bị lễ cúng giải hạn năm tam tai của 1970</span></span></em></strong></h3>

                        <p dir="ltr"><span style="font-size:16px"><span style="font-family:times new roman,times,serif">Những lễ vật cơ bản, không thể thiết cần chuẩn bị để cúng giải hạn tam tai tuổi Canh Tuất bao gồm một bộ quần áo , móng tay, tóc (nam 7 sợi, nữ 9 sợi), một đôi giày của người cần giải hạn và sếp lễ giải hạn cùng hương, hoa, muối, gạo của gia đình. </span></span>
                        </p>

                        <h3 dir="ltr"><strong><em><span style="font-size:16px"><span style="font-family:times new roman,times,serif">4.3. Văn khấn giải hạn tam tai tuổi Tuất 1970</span></span></em></strong></h3>

                        <p dir="ltr"><span style="font-size:16px"><span style="font-family:times new roman,times,serif">Sau khi chuẩn bị đầy đủ lễ cúng, sắp lễ đúng ngày và đúng hướng thì người cúng tuổi Canh Tuất bắt đầu đọc văn khấn như sau: </span></span>
