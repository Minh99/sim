                        <h2 dir="ltr"><strong><span style="font-size:16px"><span style="font-family:times new roman,times,serif"><span id="1-han-tam-tai-tuoi-canh-tuat-la-gi-anh-huong-cua-tam-tai-1970-nhu-the-nao">1. Hạn tam tai tuổi Canh Tuất là gì? Ảnh hưởng của tam tai 1970 như thế nào?</span></span></span></strong></h2>

                        <p dir="ltr"><span style="font-size:16px"><span style="font-family:times new roman,times,serif">Hạn tam tai là hạn 3 năm liên tiếp của từng tuổi. Cứ 12 năm thì sẽ gặp một hạn tam tai, trong đó, năm thứ 2 của hạn thường là nặng nhất.</span></span>
                        </p>

                        <p dir="ltr"><span style="font-size:16px"><span style="font-family:times new roman,times,serif">Tam tai bao gồm 3 loại tai họa lần lượt là: Hỏa tai, Thủy tai và Phong tai. Bên cạnh đó còn có Tiểu Tam Tai, tức 3 loại tai họa nhỏ, bao gồm: Cơ cẩn chi (mất mùa); Tật dịch tai (dịch bệnh); Đao binh tai (chiến tranh).</span></span>
                        </p>

                        <p dir="ltr"><span style="font-size:16px"><span style="font-family:times new roman,times,serif">Ảnh hưởng của hạn tam tai tuổi Canh Tuất 1970 cũng như các tuổi khác có thể nhẹ như gặp trở ngại, khó khăn, rủi ro, bạo bệnh, hao tiền tốn của. Nhưng hạn nặng thì có thể có tang trong gia đình, tai nạn xe cộ gây thương tích nghiêm trọng,.. Do đó, người tuổi trong hạn Tam tai cần cẩn trọng, không nên khởi sự vào năm bị Tam tai. </span></span>
                        </p>

