                    <p dir="ltr" style="text-align:center"><span style="color:#B22222"><strong><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif"><img alt="1994 hợp màu gì" src="https://simhoptuoi.com.vn/media/images/news/387/1994%20h%E1%BB%A3p%20m%C3%A0u%20g%C3%AC%20(2).jpg" /></span></span>
