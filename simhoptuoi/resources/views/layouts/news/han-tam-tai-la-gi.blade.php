                    <p> </p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:16px"><span style="font-family:times new roman,times,serif">- Trước khi cúng giải hạn tam tai bạn cần chuẩn bị bài vị theo mẫu của từng năm, bài vị in trên bìa giấy đỏ, chữ đen và dán trên que dài, cắm trên cốc gạo. Bài vị cần đặt ngay giữa bàn lễ và đối diện với người đứng cúng.</span></span></p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:16px"><span style="font-family:times new roman,times,serif">- Tiếp đó, bạn cần cắt chút tóc hoặc móng tay, móng chân người cần giải hạn Tam tai buộc với ít bạc lẻ sau đó đặt trên bàn lễ.</span></span></p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:16px"><span style="font-family:times new roman,times,serif">- Trên bàn lễ cần chuẩn bị 1 bộ tam sênh bao gồm: 1 con tôm luộc, 1 quả trứng vịt lộn, 1 miếng thịt luộc. Tiếp đó là 3 nén hương, 3 miếng trầu cau, 3 điều thuốc, 3 ly rượu nhỏ, 2 xấp tiền vàng các loại và 3 cây nến, 1 đĩa hoa quả, 1 bình hoa, 1 đĩa gạo muối và 2 bộ đồ thế (nam hoặc nữ tùy theo giới tính người giải hạn)</span></span></p>

