                    <h2 dir="ltr" style="text-align:justify"><span style="color:#FF0000"><strong><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif"><span id="3-trong-5-nam-toi-bo-me-cung-tuoi-giap-ty-sinh-con-nam-nao-thi-hop">3. Trong 5 năm tới, bố mẹ cùng tuổi Giáp Tý sinh con năm nào thì hợp?</span></span>
                                </span>
                            </strong>
                        </span>
                    </h2>

                    <h3 dir="ltr" style="text-align:justify"><em><strong><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">3.1. Bố mẹ tuổi Giáp Tý sinh con năm 2023</span></span></strong></em></h3>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Con sinh năm 2023 là tuổi Quý Mão, bản mệnh Kim Bạch Kim, thiên can Quý, địa chi Mão. Ứng dụng cách xem tuổi Giáp Tý sinh con năm nào hợp thì thấy: </span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">+ Ngũ hành sinh khắc: con mệnh Kim bình hòa với bố mẹ mệnh Kim.</span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">+ Thiên can: con can Quý có quan hệ Bình Hòa với can Giáp của bố mẹ.</span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">+ Địa chi: con tuổi Mão tứ hành xung với tuổi bố mẹ tuổi Tý. </span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">=> Kết luận: Năm 2023 được đánh giá là năm trung bình với bố mẹ tuổi Giáp Tý có dự định sinh con, nên cân nhắc sinh con vào năm tốt hơn. </span></span>
                    </p>

                    <h3 dir="ltr" style="text-align:justify"><em><strong><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">3.2. Bố mẹ tuổi Giáp Tý sinh con năm 2024</span></span></strong></em></h3>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Đứa trẻ chào đời vào năm 2024 có tuổi âm lịch là Giáp Thìn, thuộc mệnh Hỏa, thiên can Giáp và địa chi Thìn. Xét 3 yếu tố luận năm sinh hợp tuổi thấy:</span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">+ Ngũ hành sinh khắc: con mệnh Hỏa xung khắc với bố mẹ mệnh Kim</span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">+ Thiên can: con can Giáp bình hòa với bố mẹ can Giáp</span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">+ Địa chi: tuổi Thìn và tuổi Tý nằm trong tam hợp.</span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">=> Kết luận: Năm 2024 là năm bình hòa cho kế hoạch sinh con của tuổi Giáp Tý. </span></span>
                    </p>

                    <h3 dir="ltr" style="text-align:justify"><em><strong><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">3.3. Bố mẹ tuổi Giáp Tý sinh con năm 2025</span></span></strong></em></h3>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Con sinh năm 2025 thì tuổi âm lịch là Ất Tỵ, ngũ hành bản mệnh Hỏa, thiên can Ất, địa chi Tỵ. Dựa vào các yếu tố luận năm sinh con thấy:</span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">+ Ngũ hành sinh khắc: con bản mệnh Hỏa khắc với bố mẹ bản mệnh Kim.</span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">+ Thiên can: con can Ất bình hòa với bố mẹ can Giáp</span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">+ Địa chi: tuổi Tỵ và tuổi Tý phạm Tứ Tuyệt.</span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">=> Kết luận: Năm 2025 là năm xấu, không nên sinh con với tuổi Giáp Tý. Nếu sinh con vào năm này chẳng những ảnh hưởng đến sự nghiệp của bố mẹ, mà tình cảm vợ chồng cũng dễ xảy ra xung đột, rạn nứt. Ngoài ra thì vận số người con cũng ít may mắn, phải trải qua nhiều thăng trầm, biến cố trên đường đời. </span></span>
                    </p>

                    <h3 dir="ltr" style="text-align:justify"><em><strong><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">3.4. Bố mẹ tuổi Giáp Tý sinh con năm 2026</span></span></strong></em></h3>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Tuổi Giáp Tý sinh con năm 2026 thì tuổi âm lịch của con là Bính Ngọ, bản mệnh Thủy, thiên can Bính, địa chi Ngọ. Xét ngũ hành bản mệnh, thiên can, địa chi của con và bố mẹ thấy:</span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">+ Ngũ hành sinh khắc: con mang bản mệnh Thủy kết hợp với bố mẹ mệnh Kim là quan hệ Tương Sinh.</span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">+ Thiên can: con thiên can Bính có quan hệ Bình Hòa với can Giáp của bố mẹ 1984.</span></span>
                    </p>

