                    <h2 dir="ltr" style="text-align:justify"><span style="color:#FF0000"><strong><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif"><span id="1-tai-sao-nen-xem-tuoi-giap-ty-sinh-con-nam-nao-thi-hop">1. Tại sao nên xem tuổi Giáp Tý sinh con năm nào thì hợp?</span></span>
                                </span>
                            </strong>
                        </span>
                    </h2>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Sinh con hợp tuổi Giáp Tý mang nhiều ý nghĩa quan trọng. Khi tuổi mệnh của con và bố mẹ có sự hài hòa thì sẽ đem đến cát khí may mắn, bổ trợ cho công danh sự nghiệp của bố mẹ có sự thăng tiến, ngày càng phát triển. Đồng thời còn giúp tình cảm gia đạo gắn kết, vợ chồng, con cái thuận hòa, vui vẻ. </span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Bên cạnh đó, tuổi Giáp Tý 1984 chọn sinh con vào năm tốt cũng chính là cách để giúp con có khởi đầu tốt đẹp. Đứa trẻ chào đời nhận được nhiều phúc khí và có sức khỏe dồi dào, vận số tương lai tươi sáng, tiền đồ rộng mở. </span></span>
                    </p>


                    <h2 dir="ltr" style="text-align:justify"><span style="color:#FF0000"><strong><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif"><span id="2-ba-tieu-chuan-xac-dinh-tuoi-giap-ty-sinh-con-nam-nao-thi-hop">2. Ba tiêu chuẩn xác định tuổi Giáp Tý sinh con năm nào thì hợp</span></span>
                                </span>
                            </strong>
                        </span>
                    </h2>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Để đánh giá tuổi Giáp Tý sinh con năm nào là tốt nhất thì phụ huynh phải nắm giá được ngũ hành - thiên can - địa chi của bản thân và năm sinh con để làm cơ sở:</span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><strong><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">- Ngũ hành sinh khắc (chiếm 40%):</span></span></strong></p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Tuổi 1984 bản mệnh Hải Trung Kim. Được biết Thổ sinh Kim, Kim sinh Thủy và cùng mệnh Kim tương hỗ cho nhau. Do đó, năm sinh con tốt cho bố mẹ tuổi Giáp Tý là năm có ngũ hành tương sinh hoặc tương hỗ với bản mệnh. Cụ thể các năm hợp sinh con là:</span></span>
                    </p>

                    <ul>
                        <li dir="ltr">
                            <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Năm có ngũ hành Thổ (con tương sinh với bố mẹ)</span></span>
                            </p>
                        </li>
                        <li dir="ltr">
                            <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Năm có ngũ hành Thủy (bố mẹ tương sinh con)</span></span>
                            </p>
                        </li>
                        <li dir="ltr">
                            <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Năm có ngũ hành Kim (bố mẹ và con bình hòa) </span></span>
                            </p>
                        </li>
                    </ul>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Cũng theo thuyết ngũ hành, Hỏa khắc Kim và Kim khắc Mộc. Do đó, bố mẹ tuổi 1994 nên tránh sinh con vào các năm có ngũ hành Hỏa hoặc hành Mộc.  </span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><strong><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">- Thiên can xung hợp (chiếm 30%):</span></span></strong></p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Nam và nữ tuổi Giáp Tý có thiên can Giáp. Vì vậy những người này nên sinh con vào năm có thiên can hợp (tốt nhất cho bố mẹ) là Kỷ. Bên cạnh đó, cũng có thể chọn sinh con vào các năm có thiên can Bình Hòa với Giáp như Ất, Bính, Đinh, Tân, Nhâm, Quý. Đồng thời tránh sinh con vào năm có thiên can là Canh và Mậu. Bởi lẽ Canh phá Giáp, Mậu xung Giáp. </span></span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><strong><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">- Địa chi xung hợp (chiếm 30%):</span></span></strong></p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Xét theo địa chi thì năm hợp với địa chi Tý là Sửu, Thân, Thìn. Trong đó:</span></span>
                    </p>

                    <ul>
                        <li dir="ltr">
                            <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Nhóm Nhị hợp: Tý - Sửu</span></span>
                            </p>
                        </li>
                        <li dir="ltr">
                            <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Nhóm Tam hợp: Tý - Thân - Thìn. </span></span>
                            </p>
                        </li>
                    </ul>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Còn năm có địa chi xung với tuổi Tý, nên tránh sinh con là các năm thuộc tứ hành xung hoặc lục phá với tuổi bao gồm:</span></span>
                    </p>

                    <ul>
                        <li dir="ltr">
                            <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Nhóm tứ hành xung:  Tý - Ngọ - Mão - Dậu; </span></span>
                            </p>
                        </li>
                        <li dir="ltr">
                            <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Nhóm Lục phá: Tý - Dậu, Lục Hại: Tý - Mùi.</span></span>
                            </p>
                        </li>
                    </ul>

                    <p dir="ltr" style="text-align:justify"><em><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">=><strong> Kết luận:</strong> Khi chọn năm sinh con, bố mẹ tuổi 1984 nên chọn những năm có ngũ hành tương sinh hoặc tương hỗ, thiên can tương hợp, địa chi Tam hợp hoặc Nhị Hợp là tốt nhất. Trong trường hợp không chọn được những tiêu chí tốt nhất này thì nên chọn năm sinh không phạm xung - phá, tối thiểu là Bình Hòa. </span></span></em></p>

