                    </p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">+ Đi làm ăn xa: bố mẹ đi làm ăn xa, tránh va chạm với con cái cũng là cách để hóa giải xung khắc khi tuổi con và tuổi bố mẹ không hợp nhau. Với cách này tuy rằng bố mẹ không có nhiều thời gian chăm sóc, nuôi dạy con nhưng sẽ phần nào hạn chế được những tác động tiêu cực. Nhờ đó cải thiện vận số của con cũng như của bố mẹ, giúp gia đình thuận hòa, êm ấm. </span></span>
