                        </ul>

                        <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Địa chi là một trong 3 yếu tố để xác định tuổi 1995 sinh con năm nào tốt. Yếu tố này sẽ xét mối quan hệ giữa địa chi của con và địa chi của bố mẹ, nếu nằm trong quan hệ nhị hợp hoặc tam hợp thì đó là năm sinh tốt. Áp dụng với tuổi Ất Hợi 1995 thì:</span></span>
