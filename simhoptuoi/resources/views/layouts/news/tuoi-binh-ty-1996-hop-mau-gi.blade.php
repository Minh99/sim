                                bao la. Những gam màu này mang đến cảm giác sâu thẳm, rộng lớn nhưng cũng vô cùng vững vàng và bình yên. Ngoài ra, màu xanh dương cũng là màu của sự nam tính, thể hiện trí tuệ và sự thông minh. </span>
                        </span>
                    </p>

                    <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif"><span style="color:#006400"><strong>- Màu xanh lục, xanh lá cây: </strong></span>tượng trưng cho núi rừng, cây cối. Những
                                màu này giúp chủ nhân cảm nhận được sự tươi mới, khỏe khoắn, tràn đầy sức sống mãnh liệt và không ngừng vươn cao hơn trong sự nghiệp.  </span>
