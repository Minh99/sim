                        <h3 dir="ltr" style="text-align:justify"><strong><em><span style="font-size:16px"><span style="font-family:times new roman,times,serif">2.1. Cách tính 3 năm tam tai tuổi Thìn 1976 </span></span></em></strong></h3>

                        <p dir="ltr" style="text-align:justify"><span style="font-size:16px"><span style="font-family:times new roman,times,serif">Cách tính những năm tam tai tuổi Bính Thìn 1976 cũng như những tuổi khác, đều cần dựa vào mệnh cục trong vòng tràng sinh. Trong đó, vòng tràng sinh của mỗi người tính theo chu kỳ 12 năm, còn mệnh cục nói chi tiết hơn là nhóm tam hợp mệnh cục. Vì thế những nhóm cùng tam hợp sẽ có 3 năm tam tai giống nhau, cụ thể như sau:</span></span>
                        </p>

                        <p dir="ltr" style="text-align:justify"><span style="font-size:16px"><span style="font-family:times new roman,times,serif">✔ Tam hợp Kim cục (Thìn, Dậu, Sửu) phạm tam tai vào năm Hợi, Tý, Sửu. </span></span>
                        </p>

                        <p dir="ltr" style="text-align:justify"><span style="font-size:16px"><span style="font-family:times new roman,times,serif">✔ Tam hợp Mộc cục (Hợi, Mão, Mùi) phạm tam tai vào năm Thìn, Ngọ, Mùi.</span></span>
                        </p>

                        <p dir="ltr" style="text-align:justify"><span style="font-size:16px"><span style="font-family:times new roman,times,serif">✔ Tam hợp Thủy cục (Thân, Tý, Thìn) phạm tam tai vào năm Dần, Mão, Thìn.</span></span>
                        </p>

                        <p dir="ltr" style="text-align:justify"><span style="font-size:16px"><span style="font-family:times new roman,times,serif">✔ Tam hợp Hỏa cục (Dần, Ngọ, Tuất) phạm tam tai vào năm Thân, Dậu,  Tuất.</span></span>
                        </p>

                        <p dir="ltr" style="text-align:justify"><span style="font-size:16px"><span style="font-family:times new roman,times,serif">Từ nhóm tam hợp mệnh cục có thể thấy có 4 con giáp Sửu, Mùi, Thìn, Tuất sẽ có hạn tam tai cuối trùng với năm tuổi của mình, và không có nhóm tam hợp Thổ cục. Vì trong vòng tràng sinh, hành Thổ đóng vai trò là trung tâm phân tán ra 4 địa chi Sửu, Mùi, Thìn, Tuất, và cũng là ngũ hành dung nạp được cả 4 hành còn lại. </span></span>
                        </p>

                        <p dir="ltr" style="text-align:justify"><span style="font-size:16px"><span style="font-family:times new roman,times,serif"><u><strong>Kết luận</strong></u>: Dựa theo cách tính tam tai như trên thì năm Tam Tai của 1976 (Bính Thìn) sẽ rơi vào 3 năm liên tiếp Dần - Mão - Thìn. Cụ thể là các năm Nhâm Dần - 2022, Quý Mão - 2023, Giáp Thìn - 2024, Giáp Dần - 2034, Ất Mão - 2035, Bính Thìn - 2036, Bính dần - 2046, Đinh Mão - 2047, Mậu Thìn - 2048, Mậu Dần - 2058, Kỷ Mão - 2059, Canh Thìn - 2060, Canh Dần - 2070, Tân Mão - 2071, Nhâm Thìn - 2072.. </span></span>
