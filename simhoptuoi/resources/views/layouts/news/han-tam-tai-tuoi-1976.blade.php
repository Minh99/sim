                        <p dir="ltr" style="text-align:justify"><span style="font-size:16px"><span style="font-family:times new roman,times,serif"><em><strong>- Năm Mão,</strong></em> tức năm tam tai giữa, là năm tam tai nặng nhất và ảnh hưởng trực tiếp đến quá trình làm việc. Do đó, quý bạn tuổi Bính Thìn không nên tạm dừng hay dừng hẳn các công việc đang làm dang dở, thay đổi sang công việc khác. </span></span>
                        </p>

