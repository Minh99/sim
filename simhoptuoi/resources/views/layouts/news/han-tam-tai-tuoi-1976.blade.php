                        <p dir="ltr" style="text-align:justify"><span style="font-size:16px"><span style="font-family:times new roman,times,serif">Hạn tam tai thường được nhiều người biết là loại hạn xảy ra trong 3 năm liên tục. Hạn ở đây chính là tai hại, hạn, họa hại. Hiểu giản lược nhất, tam tai là 3 tai họa. Mỗi người sống trên đời cứ qua 12 năm sẽ một lần gặp phải họa tam tai. </span></span>
