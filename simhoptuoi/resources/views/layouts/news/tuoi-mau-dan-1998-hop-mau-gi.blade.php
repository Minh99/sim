                        </h2>

                        <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Bên cạnh việc luận tuổi 1998 hợp màu gì như trên, thì tùy theo từng năm mà người dùng có thể kết hợp phối màu theo tương quan ngũ hành năm đương kim với mệnh quái. Các chuyên gia phong thủy cho biết, trong năm Quý Mão 2023 ngũ hành Kim, nam tuổi Mậu Dần nên tăng cường sử dụng màu vàng, nâu, hạn chế bớt màu đỏ, cam, hồng tím. Còn nữ sinh năm 1997 nên dùng nhiều màu xanh lá cây, giảm bớt màu đen, xanh dương. Việc tăng giảm màu hợp tuổi 1998 này sẽ giúp chủ mệnh nhân được vượng khí từ năm 2023.</span></span>
                        </p>

                        <p dir="ltr" style="text-align:justify"><span style="font-size:14px"><span style="font-family:arial,helvetica,sans-serif">Tuy nhiên năm 2023 chỉ có 365 ngày nên ứng dụng tương quan ngũ hành màu sắc trong năm đương kim chỉ nên phối hợp với việc mang tính ngắn hạn như đổi màu tóc, màu điện thoại, màu trang phục, phụ kiện,...Nhờ đó sẽ giúp chủ nhân luôn được thuận lợi, hanh thông trong tình duyên, sự nghiệp và cả các phương diện khác của cuộc sống. </span></span>
